export const calculateTotals = (items: InventoryItem[]): InventoryTotals => {
  return items.reduce(
    (totals, item) => ({
      inbound: totals.inbound + item.inbound,
      outbound: totals.outbound + item.outbound,
      adjustment: totals.adjustment + item.adjustment,
      onHand: totals.onHand + item.onHand,
      available: totals.available ? totals.available + (item.available || 0) : item.available || 0,
      onHold: totals.onHold ? totals.onHold + (item.onHold || 0) : item.onHold || 0,
    }),
    { inbound: 0, outbound: 0, adjustment: 0, onHand: 0, available: 0, onHold: 0 },
  )
}
